def plot_irregularity(city_report_path: Path, manifest_path: Path, output_dir: Path) -> None:
    if not city_report_path.exists() or not manifest_path.exists():
        return

    manifest = load_manifest(manifest_path)
    provider_lookup = city_provider_lookup(manifest)
    providers = []
    bunch_values = []
    wide_gap_values = []

    with city_report_path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        for row in reader:
            provider = provider_lookup.get(int(row["city_id"]), f"Provider {row['city_id']}")
            providers.append(provider)
            bunch_values.append(float(row["bunch_rate"]))
            wide_gap_values.append(float(row["wide_gap_rate"]))

    if not providers:
        return

    x_values = list(range(len(providers)))
    width = 0.36

    plt.figure(figsize=(10, 5.5))
    plt.bar([x - width / 2 for x in x_values], bunch_values, width=width, label="Bunch-rate proxy", color="#7a5195")
    plt.bar([x + width / 2 for x in x_values], wide_gap_values, width=width, label="Wide-gap proxy", color="#ef5675")
    plt.xticks(x_values, providers)
    plt.ylabel("Rate")
    plt.title("Schedule Irregularity Proxies by Provider")
    plt.ylim(0, 0.9)
    plt.legend()
    plt.tight_layout()
    plt.savefig(output_dir / "provider_irregularity.png", dpi=200)
    plt.close()


