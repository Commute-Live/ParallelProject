def load_results(path: Path) -> list[dict[str, object]]:
    rows: list[dict[str, object]] = []
    with path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        for row in reader:
            row["ranks"] = int(row["ranks"])
            row["records"] = int(row["records"])
            row["cities"] = int(row["cities"])
            for key in (
                "io_seconds",
                "boundary_seconds",
                "compute_seconds",
                "reduce_seconds",
                "total_seconds",
                "mean_peak_share",
                "mean_headway_seconds",
                "mean_headway_cv",
            ):
                row[key] = float(row[key])
            rows.append(row)
    return rows


def load_manifest(path: Path) -> dict[str, object]:
    with path.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def filter_rows(rows: list[dict[str, object]], study: str, backend: str) -> list[dict[str, object]]:
    return sorted(
        [row for row in rows if row["study"] == study and row["backend"] == backend and row["status"] == "SUCCESS"],
        key=lambda row: int(row["ranks"]),
    )


def plot_scaling(rows: list[dict[str, object]], study: str, output_dir: Path) -> None:
    plt.figure(figsize=(8, 5))
    for backend, color in (("cpu", "#c65d07"), ("cuda", "#0b6e4f")):
        series = filter_rows(rows, study, backend)
        if not series:
            continue
        x_values = [row["ranks"] for row in series]
        y_values = [row["total_seconds"] for row in series]
        plt.plot(x_values, y_values, marker="o", linewidth=2, label=backend.upper(), color=color)

    plt.xticks(sorted({int(row["ranks"]) for row in rows if row["study"] == study}))
    plt.xlabel("MPI Ranks")
    plt.ylabel("Total Time (s)")
    plt.title(f"{study.capitalize()} Scaling: MPI CPU vs MPI+CUDA")
    plt.grid(True, linestyle="--", linewidth=0.5, alpha=0.5)
    plt.legend()
    plt.tight_layout()
    plt.savefig(output_dir / f"{study}_scaling_total_time.png", dpi=200)
    plt.close()


def plot_speedup(rows: list[dict[str, object]], study: str, output_dir: Path) -> None:
    cpu_rows = {int(row["ranks"]): row for row in filter_rows(rows, study, "cpu")}
    cuda_rows = {int(row["ranks"]): row for row in filter_rows(rows, study, "cuda")}
    shared_ranks = sorted(set(cpu_rows) & set(cuda_rows))
    if not shared_ranks:
        return

    plt.figure(figsize=(8, 5))
    speedups = [cpu_rows[r]["total_seconds"] / cuda_rows[r]["total_seconds"] for r in shared_ranks]
    plt.plot(shared_ranks, speedups, marker="o", linewidth=2, color="#1f4e79")
    plt.xticks(shared_ranks)
    plt.xlabel("MPI Ranks")
    plt.ylabel("Speedup (MPI CPU / MPI+CUDA)")
    plt.title(f"{study.capitalize()} Scaling: GPU Speedup over CPU")
    plt.grid(True, linestyle="--", linewidth=0.5, alpha=0.5)
    plt.tight_layout()
    plt.savefig(output_dir / f"{study}_gpu_speedup.png", dpi=200)
    plt.close()


def plot_breakdown(rows: list[dict[str, object]], study: str, output_dir: Path) -> None:
    series = filter_rows(rows, study, "cuda")
    if not series:
        return

    ranks = [row["ranks"] for row in series]
    io_values = [row["io_seconds"] for row in series]
    comm_values = [row["boundary_seconds"] + row["reduce_seconds"] for row in series]
    compute_values = [row["compute_seconds"] for row in series]

    plt.figure(figsize=(8, 5))
    plt.bar(ranks, io_values, label="MPI I/O", color="#457b9d")
    plt.bar(ranks, comm_values, bottom=io_values, label="MPI Communication", color="#a8dadc")
    plt.bar(
        ranks,
        compute_values,
        bottom=[io + comm for io, comm in zip(io_values, comm_values)],
        label="CUDA Compute",
        color="#1d3557",
    )
    plt.xticks(ranks)
    plt.xlabel("MPI Ranks")
    plt.ylabel("Time (s)")
    plt.title(f"{study.capitalize()} Scaling: MPI+CUDA Runtime Breakdown")
    plt.legend()
    plt.tight_layout()
    plt.savefig(output_dir / f"{study}_cuda_breakdown.png", dpi=200)
    plt.close()


